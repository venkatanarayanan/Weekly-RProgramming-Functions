# Create a sample string
sample_string <- "John is 8 years old"

# Use 'nchar()' to count the number of elements(letters) in the string
nchar(sample_string)

# Create a sample character vector
sample_char_vector <- c("James", "John", "David", "Peter")

# Use 'nchar()' to count the number of elements(letters) in each element of the vector
nchar(sample_char_vector)
