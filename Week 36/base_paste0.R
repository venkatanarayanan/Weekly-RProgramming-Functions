# Concatenate a sample sentence using paste0()
sample_sentence <- paste0("This", "is", "my", "name")

# print the sentence to see how it has been concatenated
sample_sentence

# The paste0() function has no seperator
sample_sentence <- paste0("This", " ","is", " " ,"my", " ","name")

